package Discord.playerHandlers;

/**
 * <p>RepeatState class.</p>
 *
 * @author xXTheSebXx
 * @version 1.0-SNAPSHOT
 */
public enum RepeatState {
    /**
     * State if not repeating
     */
    NO_REPEAT,
    /**
     * state if repeating whole queue
     */
    REPEAT_QUEUE,
    /**
     * state if repeating a single song
     */
    REPEAT_SINGLE;
}
